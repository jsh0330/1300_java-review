package forTest;

public class ForTest02 {
	public static void main(String[] args) {
//		1번 장서혁
//		2번 장서혁
//		...
//		5번 장서혁
		
//		1. for (int i = 1; i < 6; i++) {
//		2. 출력
		
		for (int i = 1; i < 6; i++) {
			System.out.println(i + "번 장서혁");
		}
		
//		10번째 장서혁 ~ 1번째 장서혁
//		1. for (int i = 10; i > 0; i--) {
//		2. 출력
		
		for (int i = 10; i > 0; i--) {
			System.out.println(i + "번 장서혁");
		}
	}

}
